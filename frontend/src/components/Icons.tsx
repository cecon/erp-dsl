/**
 * Resolves an icon name to a ReactNode SVG element.
 * Falls back to a generic file icon when the name is unknown.
 */
export function getIcon(name?: string): ReactNode {
  if (!name) return fileText;
  return ICONS[name] ?? fileText;
}
